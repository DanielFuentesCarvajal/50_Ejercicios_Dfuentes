#Por como funcionan las formulas del movimiento rectilineo uniformemente acelerado, es imposible que se solucione esto sin tener la Velocidad inicial o la distancia, asi que asumi que la velocidad incial es de 0
a=float(input("Ingrese la aceleración del objeto(En m/s²) "))
t=float(input("Ingrese el tiempo del recorrido(En segundos) "))
Vf=a*t
print("La velocidad final del objeto es de", Vf,"m/s")