A=float(input("Ingrese el primer numero: "))
B=float(input("Ingrese el segundo numero: "))
C=float(input("Ingrese el tercer nombre: "))
if A==B:
    print("El primer numero es igual al segundo")
else:
    if A==C:
        print("El primero numero es igual al tercero")
    else:
        if B==C:
            print("El segundo numero es igual al tercero")
        else:
            print("No existen dos numeros iguales")